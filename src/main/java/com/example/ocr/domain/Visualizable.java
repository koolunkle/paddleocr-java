package com.example.ocr.domain;

/**
 * OCR 처리 결과를 원본 이미지 위에 시각화(Bounding Box, Label 등)할 때 
 * 필요한 필수 속성을 정의하는 공통 인터페이스입니다.
 * LayoutType, DecisionField 등의 도메인 객체(Enum)가 이 인터페이스를 구현하여,
 * VisualService가 도메인에 결합(Coupling)되지 않고 독립적으로 동작하도록 돕습니다.
 */
public interface Visualizable {

    // ========================================================================
    // 공개 추상 메서드 
    // ========================================================================

    /**
     * 시각화 시 테두리나 배경 등에 사용할 색상 성분을 반환합니다.
     * OpenCV 라이브러리의 특성에 맞추어, 기본적으로 BGR 포맷의 
     * 길이가 3인 double 배열을 반환해야 합니다.
     *
     * @return BGR 색상 배열 (예: {255.0, 0.0, 0.0} = 파란색)
     */
    double[] getColorComponents();

    /**
     * 시각화 대상의 고유 식별 키 또는 화면에 표시할 라벨 명칭을 반환합니다.
     * 박스 위에 출력될 텍스트나, 매핑 로직에서 객체를 식별하는 용도로 사용됩니다.
     *
     * @return 도메인 식별 키 (예: "table", "채권자" 등)
     */
    String getKey();
}